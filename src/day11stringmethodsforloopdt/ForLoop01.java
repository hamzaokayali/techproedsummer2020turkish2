package day11stringmethodsforloopdt;

public class ForLoop01 {

	public static void main(String[] args) {

	for (int i =0 ; i<=10 ; i++) {
		System.out.println("java"+i);
		
	}
	for (int i =1 ; i<=7 ; i++) {
		System.out.println(i);
		}
		
	for (int i =1 ; i<=100 ; i++) {
		System.out.println(i*2);
		}
		
	}

}
