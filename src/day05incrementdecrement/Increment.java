package day05incrementdecrement;

public class Increment {

	public static void main(String[] args) {
		
		int s = 12;
		
		s = s + 3;
		System.out.println(s);
		
		int t = 22;
		System.out.println(t);
		t+=3;
		System.out.println(t);
		int u=55;
		u++;
		System.out.println(u);
		
		int d =5;
		d=d*2;
		
		System.out.println(d);
		
		int y =9;
		y*=2;
		System.out.println(y);
		
		

	}

}
